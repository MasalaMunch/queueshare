S = require(`./SyncedTree.js`);
V = require(`./Version.js`);

newerV = V.Newer(V.oldest);

const MyS = class extends S {

    _write (change) {

        console.log(`writing to`, change.path);

        super._write(change);

    }

};

s = new MyS();

s.receive({path: [5], versions: [newerV, newerV]});

s.receive({path: [], versions: [newerV]});