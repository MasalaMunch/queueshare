"use strict";

const AbstractTask = require(`abstract-task`);

module.exports = class extends AbstractTask {

    do () {

        super.do();

        try {

            this.output = this.f();

            this.isDone = true;

        } catch (error) {

            this.error = error;

            this.threwAnError = true;

        }

        this.broadcastChange();

    }

    };